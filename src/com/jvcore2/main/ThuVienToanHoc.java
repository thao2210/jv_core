package com.jvcore2.main;

public class ThuVienToanHoc {
	public static int tongHaiSo(int x, int y) { 
		int tong = x + y;
		return tong;
	}
	
	public static double tongHaiSo(double x, double y) { 
		double tong = x + y;
		return tong;
	}
	
	public static int hieuHaiSo(int x, int y) { 
		int hieu = x - y;
		return hieu;
	}
	
	public static double hieuHaiSo(double x, double y) { 
		double hieu = x - y;
		return hieu;
	}
	
	public static double tichHaiSo(double x, double y) { 
		double tich = x * y;
		return tich;
	}
	
	public static int tichHaiSo(int x, int y) { 
		int tich = x * y;
		return tich;
	}
	
	public static double thuongHaiSo(double x, double y) { 
		double thuong = x / y;
		return thuong;
	}
	
	public static int thuongHaiSo(int x, int y) { 
		int thuong = x / y;
		return thuong;
	}
}
